<template>
  <div>
    <ul>
      <li v-for="m in messageList" :key="m.id">
        <!-- 跳转路由并携带params参数，to的字符串写法 -->
        <!-- <router-link :to="`/home/message/detail/${m.id}/${m.title}`">{{
          m.title
        }}</router-link> -->

        <!-- 跳转路由并携带params参数，to的对象 不能使用path，只能使用name-->
        <router-link
          :to="{
            name: 'xiangqing',
            params: { id: m.id, title: m.title },
          }"
          >{{ m.title }}</router-link
        >
        <button @click="pushShow(m)">push查看</button>&nbsp;
        <button @click="replaceShow(m)">replace查看</button>
      </li>
    </ul>

    <hr />
    <router-view></router-view>
  </div>
</template>

<script>
export default {
  name: "Message",
  data() {
    return {
      messageList: [
        { id: "001", title: "消息1" },
        { id: "002", title: "消息2" },
        { id: "003", title: "消息3" },
      ],
    };
  },
  methods: {
    pushShow(m) {
      // console.log(this.$router);
      this.$router.push({
        name: "xiangqing",
        params: { id: m.id, title: m.title },
      });
    },
    replaceShow(m) {
      // console.log(this.$router);
      this.$router.replace({
        name: "xiangqing",
        params: { id: m.id, title: m.title },
      });
    },
  },
};
</script>
